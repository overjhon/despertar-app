// FunÃ§Ã£o para substituir placeholders
function fillTemplate(template: string, variables: ContentVariables): string {
  let result = template;
  const placeholders = template.match(/\{[^}]+\}/g) || [];
  
  for (const placeholder of placeholders) {
    const key = placeholder.slice(1, -1); // Remove { e }
    const options = variables[key];
    
    if (options && options.length > 0) {
      const randomValue = options[Math.floor(Math.random() * options.length)];
      result = result.replace(placeholder, randomValue);
    }
  }
  
  return result;
}

// Gerar post realista
export function generateRealisticPost(): string {
  const template = postTemplates[Math.floor(Math.random() * postTemplates.length)];
  return fillTemplate(template, variables);
}

